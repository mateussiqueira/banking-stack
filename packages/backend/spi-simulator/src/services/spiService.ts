function generateEndToEndId(): string {
  const timestamp = Date.now().toString(36).toUpperCase()
  const random = uuidv4().replace(/-/g, '').slice(0, 8).toUpperCase()
  return `E2E${timestamp}${random}`
}

function isValidIspb(ispb: string): boolean {
  return VALID_ISPBS.has(ispb)
}

export function processPayment(xml: string): {
  transaction: Transaction
  statusXml: string
} {
  const parsed = parsePacs008(xml)

  if (!isValidIspb(parsed.debtorIspb)) {
    throw new Error(`Invalid debtor ISPB: ${parsed.debtorIspb}`)
  }

  if (!isValidIspb(parsed.creditorIspb)) {
    throw new Error(`Invalid creditor ISPB: ${parsed.creditorIspb}`)
  }

  if (parsed.amount <= 0) {
    throw new Error('Amount must be positive')
  }

  if (parsed.amount > 100000000) {
    throw new Error('Amount exceeds maximum allowed (R$ 100,000,000)')
  }

  const endToEndId = parsed.endToEndId || generateEndToEndId()

  const existing = getTransaction(endToEndId)
  if (existing) {
    throw new Error(`Duplicate endToEndId: ${endToEndId}`)
  }

  const transaction: Transaction = {
    id: uuidv4(),
    endToEndId,
    txId: parsed.txId || uuidv4(),
    amount: parsed.amount,
    creditorIspb: parsed.creditorIspb,
    creditorKey: parsed.creditorKey,
    creditorName: parsed.creditorName,
    debtorIspb: parsed.debtorIspb,
    debtorKey: parsed.debtorKey,
    debtorName: parsed.debtorName,
    status: 'ACCEPTED',
    createdAt: new Date().toISOString(),
  }

  addTransaction(transaction)

  const statusXml = buildPacs002(transaction, 'ACCEPTED')

  return { transaction, statusXml }
}

export function settlePayment(endToEndId: string): {
  transaction: Transaction
  statusXml: string
} {
  const tx = getTransaction(endToEndId)
  if (!tx) {
    throw new Error(`Transaction not found: ${endToEndId}`)
  }

  if (tx.status !== 'ACCEPTED') {
    throw new Error(
      `Transaction ${endToEndId} is in status ${tx.status}, expected ACCEPTED`
    )
  }

  const updated = updateTransaction(endToEndId, {
    status: 'SETTLED',
    settledAt: new Date().toISOString(),
  })

  if (!updated) {
    throw new Error(`Failed to update transaction: ${endToEndId}`)
  }

  const statusXml = buildPacs002(updated, 'SETTLED')

  return { transaction: updated, statusXml }
}

export function returnPayment(
  endToEndId: string,
  reason?: string,
  additionalInfo?: string
): { transaction: Transaction; returnXml: string } {
  const originalTx = getTransaction(endToEndId)
  if (!originalTx) {
    throw new Error(`Original transaction not found: ${endToEndId}`)
  }

  if (originalTx.status !== 'SETTLED') {
    throw new Error(
      `Transaction ${endToEndId} is in status ${originalTx.status}, expected SETTLED`
    )
  }

  const returnEndToEndId = generateEndToEndId()

  const returnTx: Transaction = {
    id: uuidv4(),
    endToEndId: returnEndToEndId,
    txId: uuidv4(),
    amount: originalTx.amount,
    creditorIspb: originalTx.debtorIspb,
    creditorKey: originalTx.debtorKey,
    debtorIspb: originalTx.creditorIspb,
    debtorKey: originalTx.creditorKey,
    status: 'RETURNED',
    returnReason: reason || 'FRAD',
    returnRejectionReason: additionalInfo || 'Return requested',
    createdAt: new Date().toISOString(),
    returnedAt: new Date().toISOString(),
    originalEndToEndId: endToEndId,
  }

  addTransaction(returnTx)

  updateTransaction(endToEndId, {
    status: 'RETURNED',
    returnedAt: new Date().toISOString(),
  })

  const returnXml = buildPacs004(returnTx)

  return { transaction: returnTx, returnXml }
}

